class SymphonyStudio {
    constructor() {
        // UI Elements
        this.navButtons = document.querySelectorAll('.nav-btn');
        this.modeSections = document.querySelectorAll('.mode-section');
        this.corpusInput = document.getElementById('corpus-input');
        this.ngramSelect = document.getElementById('ngram-order');
        this.lengthSlider = document.getElementById('poem-length');
        this.lengthValDisplay = document.getElementById('length-val');
        this.generateBtn = document.getElementById('generate-btn');
        this.poemDisplay = document.getElementById('poem-display');
        this.resetBtn = document.getElementById('reset-corpus');
        this.copyBtn = document.getElementById('copy-btn');
        this.tokenCountDisplay = document.getElementById('token-count');

        // Mode 2 Elements
        this.seedPool = document.getElementById('seed-word-pool');
        this.activeSeedsContainer = document.getElementById('active-seeds');

        // Mode 3 Elements
        this.interactiveEditor = document.getElementById('interactive-editor');
        this.suggestionBox = document.getElementById('suggestion-box');
        this.suggestionText = document.getElementById('suggestion-text');

        // State
        this.activeMode = 'mode-corpus';
        this.selectedSeeds = new Set();
        this.currentChain = new Map();
        this.currentTokens = [];
        this.currentRecommendation = "";

        console.log("🛠️ SymphonyStudio Class Initializing...");
        try {
            this.init();
            console.log("✅ SymphonyStudio Initialization Complete");
        } catch (e) {
            console.error("❌ SymphonyStudio Initialization Failed:", e);
        }
    }

    init() {
        console.log("🚀 Starting init()...");
        
        try {
            this.corpusInput.value = localStorage.getItem('symphony_corpus') || DEFAULT_CORPUS;
            this.updateModel();
        } catch (e) { console.warn("Failed to load corpus:", e); }

        // Nav Handling
        this.navButtons.forEach(btn => {
            btn.addEventListener('click', () => {
                console.log("Nav clicked:", btn.dataset.mode);
                this.switchMode(btn.dataset.mode);
            });
        });

        // Corpus Listeners
        if (this.corpusInput) {
            this.corpusInput.addEventListener('input', () => {
                this.updateModel();
                localStorage.setItem('symphony_corpus', this.corpusInput.value);
            });
        }

        // Global Settings
        if (this.ngramSelect) {
            this.ngramSelect.addEventListener('change', () => this.updateModel());
        }
        
        if (this.lengthSlider) {
            this.lengthSlider.addEventListener('input', () => {
                if (this.lengthValDisplay) this.lengthValDisplay.textContent = `${this.lengthSlider.value} words`;
            });
        }

        // Actions
        if (this.generateBtn) {
            this.generateBtn.onclick = () => {
                console.log("Generate button clicked!");
                this.handleGenerate();
            };
        }
        
        if (this.resetBtn) {
            this.resetBtn.onclick = () => {
                this.corpusInput.value = DEFAULT_CORPUS;
                this.updateModel();
                localStorage.setItem('symphony_corpus', DEFAULT_CORPUS);
            };
        }

        // Mode 3: Interactive
        if (this.interactiveEditor) {
            this.interactiveEditor.addEventListener('input', () => this.handleInteractiveInput());
            this.interactiveEditor.addEventListener('keydown', (e) => this.handleInteractiveKeys(e));
        }

        if (this.copyBtn) {
            this.copyBtn.addEventListener('click', () => {
                navigator.clipboard.writeText(this.poemDisplay.innerText);
                this.copyBtn.textContent = "Copied!";
                setTimeout(() => this.copyBtn.textContent = "Copy to Clipboard", 2000);
            });
        }

        try {
            this.populateSeeds();
        } catch (e) { console.warn("Failed to populate seeds:", e); }
    }

    switchMode(modeId) {
        this.activeMode = modeId;
        this.navButtons.forEach(b => b.classList.toggle('active', b.dataset.mode === modeId));
        this.modeSections.forEach(s => s.classList.toggle('active', s.id === modeId));

        // Hide/Show generate button based on mode (Mode 3 doesn't use it as much)
        this.generateBtn.style.display = modeId === 'mode-interactive' ? 'none' : 'block';
    }

    updateModel() {
        console.log("♻️ Updating Markov Model...");
        if (!this.corpusInput) {
            console.warn("⚠️ corpus-input not found");
            return;
        }
        const text = this.corpusInput.value || "";
        this.currentTokens = text.toLowerCase().match(/[\w']+|[.,!?;]/g) || [];
        
        if (this.tokenCountDisplay) {
            this.tokenCountDisplay.textContent = `Tokens: ${this.currentTokens.length}`;
        }

        const n = (this.ngramSelect) ? (parseInt(this.ngramSelect.value) || 2) : 2;
        this.currentChain = this.buildChain(this.currentTokens, n);
        this.populateSeeds();
        console.log(`📊 Model Updated: ${this.currentTokens.length} tokens, ${this.currentChain.size} unique states.`);
    }

    buildChain(tokens, n) {
        const chain = new Map();
        for (let i = 0; i < tokens.length - n; i++) {
            const state = tokens.slice(i, i + n).join(' ');
            const nextWord = tokens[i + n];
            if (!chain.has(state)) chain.set(state, []);
            chain.get(state).push(nextWord);
        }
        return chain;
    }

    // --- Mode 2: Seeds ---
    populateSeeds() {
        const uniqueWords = [...new Set(this.currentTokens)].sort();
        // Pick some interesting nouns/adjectives (heuristically: words longer than 3 chars)
        const candidates = uniqueWords.filter(w => w.length > 4);
        const displaySeeds = candidates.slice(0, 30); // Show top 30

        this.seedPool.innerHTML = '';
        displaySeeds.forEach(word => {
            const chip = document.createElement('span');
            chip.className = 'chip';
            chip.textContent = word;
            chip.onclick = () => this.toggleSeed(word, chip);
            this.seedPool.appendChild(chip);
        });
    }

    toggleSeed(word, chip) {
        if (this.selectedSeeds.has(word)) {
            this.selectedSeeds.delete(word);
            chip.classList.remove('active');
        } else {
            this.selectedSeeds.add(word);
            chip.classList.add('active');
        }
        this.renderActiveSeeds();
    }

    renderActiveSeeds() {
        this.activeSeedsContainer.innerHTML = '';
        this.selectedSeeds.forEach(word => {
            const chip = document.createElement('span');
            chip.className = 'badge';
            chip.textContent = word;
            this.activeSeedsContainer.appendChild(chip);
        });
    }

    // --- Mode 3: Interactive ---
    handleInteractiveInput() {
        const text = this.interactiveEditor.value.trim();
        if (!text) {
            if (this.currentTokens.length > 0) {
                this.currentRecommendation = this.currentTokens[Math.floor(Math.random() * this.currentTokens.length)];
                this.suggestionText.textContent = this.currentRecommendation;
                this.showSuggestion(true);
            } else {
                this.showSuggestion(false);
            }
            return;
        }

        const tokens = text.toLowerCase().match(/\b\w+\b/g) || [];
        if (tokens.length === 0) {
            this.showSuggestion(false);
            return;
        }

        // BACK-OFF STRATEGY: Try N=3, N=2, N=1, then Random
        let matchFound = false;
        const potentialOrders = [3, 2, 1];

        for (const n of potentialOrders) {
            if (tokens.length >= n) {
                const state = tokens.slice(-n).join(' ');
                // Use a temporary chain for this specific N if it's not the current model's N
                const chain = (n === parseInt(this.ngramSelect.value)) ? this.currentChain : this.buildChain(this.currentTokens, n);
                const possibleNext = chain.get(state);

                if (possibleNext && possibleNext.length > 0) {
                    const counts = {};
                    possibleNext.forEach(w => counts[w] = (counts[w] || 0) + 1);
                    this.currentRecommendation = Object.keys(counts).reduce((a, b) => counts[a] > counts[b] ? a : b);
                    matchFound = true;
                    break;
                }
            }
        }

        // Fallback: If no matches at all, pick a random word from corpus to keep the flow
        if (!matchFound && this.currentTokens.length > 0) {
            this.currentRecommendation = this.currentTokens[Math.floor(Math.random() * this.currentTokens.length)];
            matchFound = true;
        }

        if (matchFound) {
            this.suggestionText.textContent = this.currentRecommendation;
            this.showSuggestion(true);
        } else {
            this.showSuggestion(false);
        }
    }

    handleInteractiveKeys(e) {
        if (e.key === 'Tab' && this.currentRecommendation) {
            e.preventDefault();
            const text = this.interactiveEditor.value;
            this.interactiveEditor.value = text.endsWith(' ') ? text + this.currentRecommendation : text + ' ' + this.currentRecommendation;
            this.showSuggestion(false);
            this.currentRecommendation = "";
            // Trigger input for next suggestion
            this.handleInteractiveInput();
        }
    }

    showSuggestion(show) {
        this.suggestionBox.classList.toggle('hidden', !show);
    }

    // --- Generation ---
    async handleGenerate() {
        // Ensure model is updated before we try generating
        this.updateModel();

        if (this.currentTokens.length < 2 && this.activeMode !== 'mode-seed') {
            alert("Please provide some training text first!");
            return;
        }

        this.generateBtn.disabled = true;
        this.generateBtn.textContent = "🧠 Consulting Strategic Brain...";
        this.poemDisplay.innerHTML = '';

        const n = parseInt(this.ngramSelect.value) || 2;
        const length = parseInt(this.lengthSlider.value) || 40;
        let poemTokens = [];

        try {
            console.log("📡 Calling Strategic Brain API...");
            // Attempt to call the Advanced Python Brain with manual timeout
            const controller = new AbortController();
            const timeoutId = setTimeout(() => controller.abort(), 4000);

            const response = await fetch(`http://localhost:8000/generate?strategy=forward&length=${length}`, {
                method: 'GET',
                signal: controller.signal
            });
            clearTimeout(timeoutId);

            if (response.ok) {
                const data = await response.json();
                poemTokens = data.poem.split(/\s+/);
                console.log("🚀 Strategic Brain delivered the poem!");
            } else {
                throw new Error("Brain Offline");
            }
        } catch (err) {
            console.warn("⚠️ Strategic Brain offline/error. Falling back to Local JS Logic.", err);
            this.generateBtn.textContent = "Composing (Local)...";
            
            if (this.activeMode === 'mode-seed' && this.selectedSeeds.size > 0) {
                const seedsArr = Array.from(this.selectedSeeds);
                const seed = seedsArr[Math.floor(Math.random() * seedsArr.length)];
                const possibleStates = Array.from(this.currentChain.keys()).filter(s => s.includes(seed));
                if (possibleStates.length > 0) {
                    let current = possibleStates[Math.floor(Math.random() * possibleStates.length)];
                    poemTokens = this.generateFromState(current, n, length);
                } else {
                    poemTokens = this.generateNormally(n, length);
                }
            } else {
                poemTokens = this.generateNormally(n, length);
            }
        } finally {
            if (poemTokens && poemTokens.length > 0) {
                await this.animatePoem(poemTokens);
            }
            this.generateBtn.disabled = false;
            this.generateBtn.textContent = "Generate Poem";
            this.copyBtn.classList.remove('invisible');
        }
    }

    generateNormally(n, length) {
        if (!this.currentChain || this.currentChain.size === 0) {
            console.warn("Attempted to generate from empty chain.");
            return ["..."];
        }
        const states = Array.from(this.currentChain.keys());
        const start = states[Math.floor(Math.random() * states.length)];
        return this.generateFromState(start, n, length);
    }

    generateFromState(startState, n, length) {
        let poem = startState.split(' ');
        let current = startState;
        const recentTokens = [];
        const mutationChance = 0.15; // 15% chance to jump to a new theme

        for (let i = 0; i < length - n; i++) {
            // Mutation: Occasionally jump to a random state to break deterministic loops
            if (Math.random() < mutationChance) {
                const states = Array.from(this.currentChain.keys());
                current = states[Math.floor(Math.random() * states.length)];
                poem.push(...current.split(' '));
                continue;
            }

            const nextCandidates = this.currentChain.get(current);
            if (nextCandidates && nextCandidates.length > 0) {
                const scores = nextCandidates.map(word => {
                    let score = 100;
                    if (recentTokens.includes(word)) score -= 80; // Hard penalty for stuttering
                    return { word, score };
                });

                scores.sort((a,b) => b.score - a.score);
                // Variety selection: pick from top candidates
                const poolSize = Math.max(1, Math.floor(scores.length / 2));
                const candidates = scores.slice(0, poolSize).map(s => s.word);
                const next = candidates[Math.floor(Math.random() * candidates.length)];
                
                poem.push(next);
                recentTokens.push(next);
                if (recentTokens.length > 6) recentTokens.shift();
                
                current = poem.slice(-n).join(' ');
            } else {
                const states = Array.from(this.currentChain.keys());
                current = states[Math.floor(Math.random() * states.length)];
                poem.push(...current.split(' '));
            }
        }
        return poem;
    }

    async animatePoem(tokens) {
        const wordsPerLine = 6;
        let line = [];
        let capitalizeNext = false;
        for (let i = 0; i < tokens.length; i++) {
            let w = tokens[i];
            
            if (line.length === 0 && /^[.,!?;:]+$/.test(w)) continue;
            
            if (line.length === 0 || capitalizeNext) {
                w = w.charAt(0).toUpperCase() + w.slice(1);
                capitalizeNext = false;
            }
            
            if (/^[.,!?;:]+$/.test(w) && line.length > 0) {
                line[line.length - 1] += w;
                if (/[.!?;]$/.test(w)) {
                    capitalizeNext = true;
                }
                continue;
            }
            
            line.push(w);

            if (line.length >= wordsPerLine) {
                await this.addLine(line.join(' '));
                line = [];
            }
        }
        if (line.length > 0) await this.addLine(line.join(' '));
    }

    addLine(text) {
        return new Promise(r => {
            const p = document.createElement('p');
            p.textContent = text;
            p.style.opacity = '0';
            p.style.transform = 'translateY(10px)';
            p.style.transition = 'all 0.5s';
            this.poemDisplay.appendChild(p);

            requestAnimationFrame(() => {
                p.style.opacity = '1';
                p.style.transform = 'translateY(0)';
            });
            setTimeout(r, 400);
        });
    }
}

